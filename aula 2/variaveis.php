<h2>Varíaveis</h2>
<p>
    São estruturas (memorias) utilizadas
    para armazenar dados que serão processadas 
    pelo programa.

    Para criar uma variavel em PHP basta colocar 
    simbolos de $ na frente de nome e atribuir 
    um valor com o simbolo de igual. Exemplo
    <pre>
    $variavel = 10; 
    $nome = "Maria"; 
    $nota = 5.5; 
   
    </pre>
</p>

